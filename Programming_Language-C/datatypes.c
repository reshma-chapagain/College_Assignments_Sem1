//Q3. WAP to demonstrate any 6 data types
#include <stdio.h>

int main() {
    int i = 10;
    float f = 12.5;
    double d = 45.6789;
    char c = 'A';
    long l = 123456;
    unsigned int u = 50;

    printf("Integer: %d\n", i);
    printf("Float: %.2f\n", f);
    printf("Double: %.4lf\n", d);
    printf("Character: %c\n", c);
    printf("Long: %ld\n", l);
    printf("Unsigned Integer: %u\n", u);

    return 0;
}
